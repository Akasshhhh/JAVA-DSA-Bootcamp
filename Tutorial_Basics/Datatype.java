package Tutorial_Basics;

public class Datatype {
    public static void main(String[] args) {
        int rollNo = 65;
        char letter = 'e';
        float marks = 34.56f; // Need to add f at the end for float
        double largeDecimalNumbers = 45667.2235;
        long largeIntegerValues = 234565434543454L; // Need to add the L at the end of long
        boolean check = false;
        System.out.println(rollNo);
        System.out.println(letter);
        System.out.println(largeDecimalNumbers);
        System.out.println(largeIntegerValues);
        System.out.println(marks);
        System.out.println(check);
    }
}
